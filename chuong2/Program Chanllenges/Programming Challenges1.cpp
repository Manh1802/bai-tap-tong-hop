#include <iostream>
using namespace std;

int main() 
{
    // Store the integers 50 and 100 in variables
    int num1 = 50;
    int num2 = 100;

    // Store the sum of the two numbers in a variable named total
    int total = num1 + num2;

    // Display the total
    cout << "The sum of " << num1 << " and " << num2 << " is " << total << endl;

    return 0;
}

